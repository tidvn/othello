export var PlayerColour;
(function (PlayerColour) {
    PlayerColour[PlayerColour["Empty"] = 0] = "Empty";
    PlayerColour[PlayerColour["White"] = 1] = "White";
    PlayerColour[PlayerColour["Black"] = 2] = "Black";
})(PlayerColour || (PlayerColour = {}));
